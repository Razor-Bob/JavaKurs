package schleifen;

public class Level1BeginnenichtbeieinsForLoop {

		// Schreibe mit Hilfe der for-Loop ein Programm, das von fünf bis 25 zählt und die Zahl jeweils ausgibt, also 5, 6, 7, 8, 9, usw.
	
	public static void main(String[] args) {
		for (int i=5; i<=25; i++) {
			System.out.println(i);
		}

		// alleine geschafft :-)	
		// Link zur Aufgabenstellung: https://www.learnj.de/doku.php?id=einstieg:aufgabenfor:start#level_1beginn_nicht_bei_eins
	}

}
